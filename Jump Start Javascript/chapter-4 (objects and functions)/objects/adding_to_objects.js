//1st way
var obj = {};
obj["firstName"] = "Hugo"; // bracket notation // you can use variable inside bracket
obj["lastName"] = "Reyes";
console.log(obj); //output: { firstName: 'Hugo', lastName: 'Reyes' }


//2nd way
var obj = {};
obj.firstName = "Hugo"; // dot notation //can't use variable after dot
obj.lastName = "Reyes";
console.log(obj); //output: { firstName: 'Hugo', lastName: 'Reyes' }
