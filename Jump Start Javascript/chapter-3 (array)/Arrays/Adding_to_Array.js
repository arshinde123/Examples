// Adding to an Array

var myArray = [];

myArray[0] = "Hello";
myArray[1] = "World";

console.log(myArray); // output: [ 'Hello', 'World' ]

// Associative Array

var myArray_1 = [];

myArray_1["fruit"] = "apple";
myArray_1["vehicle"] = "tank";

console.log(myArray_1); // output: [ fruit: 'apple', vehicle: 'tank' ]


// In Associative Array items are stored by a named index rather than a numbered index.
//However, this is not the good practice. The preferred way to store data with named indices is by using objects
