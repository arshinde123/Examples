// creating an Array

// 1st way
var myArray_1 = new Array();

console.log(myArray_1); // output: []
// 2nd way
var myArray_2 = [];
console.log(myArray_2); // output: []

// 3rd way
var myArray_3 = [4, 8, 15, 16, 24, 42];
console.log(myArray_3); // output: [4, 8, 15, 16, 24, 42]

// 4th way
var fruit = ["apple", "orange", "pear"];
console.log(fruit); // output: ["apple", "orange", "pear"]

// 5th way
var stuff = [1, "apple", undefined, 52, "tanks", null, []];
console.log(stuff); // output: [1, "apple", undefined, 52, "tanks", null, []]
