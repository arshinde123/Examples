//Logical AND operator


console.log(true && true); // returns true

console.log(true && false); // returns false

console.log(0 && 1); // returns 0

console.log(0 && 2); // returns 0

console.log(1 && 0); // returns 0

console.log(2 && 0); // returns 0

console.log("foo" && "bar"); // returns "bar"


//Logical OR operator
console.log(true || true); // returns true

console.log(true || false); // returns true

console.log(0 || 1); // returns 1

console.log(0 || 2); // returns 2

console.log(1 || 0); // returns 1

console.log(2 || 0); // returns 2

console.log("foo" || "bar"); // returns "foo"


//Logical NOT operator
console.log(!true); //false

console.log(!false); //true

console.log(!0); //true

console.log(!1); //false

console.log(!"foo"); //false
