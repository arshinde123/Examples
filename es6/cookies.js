/*
Cookies are plain text data record of 5 variable-length fields.

    Expires − The date the cookie will expire.
              If this is blank, the cookie will expire when the visitor quits the browser.

    Domain − The domain name of your site.

    Path −  The path to the directory or web page that sets the cookie.
            This may be blank, if you want to retrieve the cookie from any directory or page.

    Secure −  If this field contains the word "secure",
              then the cookie may only be retrieved with a secure server.
              If this field is blank, no such restriction exists.

    Name = Value − Cookies are set and retrieved in the form of key-value pairs.
*/
